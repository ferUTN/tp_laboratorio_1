/** \brief Función que devuelve la suma de dos números
 *
 * \param 1er operando
 * \param 2do operando
 * \return Resultado de la suma
 *
 */
float sumar(float, float);


/** \brief Función que devuelve la resta de dos números
 *
 * \param 1er operando
 * \param 2do operando
 * \return Resultado de la resta
 *
 */
float restar(float, float);


/** \brief Función que devuelve la división entre el 1er y el 2do número
 *
 * \param 1er operando
 * \param 2do operando
 * \return Resultado de la división
 *
 */
float dividir(float, float);


/** \brief Función que devuelve la multiplicación entre dos números
 *
 * \param 1er operando
 * \param 2do operando
 * \return Resultado de la multiplicación
 *
 */
float multiplicar(float, float);


/** \brief Función que devuelve el factorial de un número (no chequea si se lo puede calcular)
 *
 * \param Número al que se le calcula el factorial
 * \return Resultado de aplicar el factorial al número
 *
 */
long long int factorial(long long int);


/** \brief Función que utiliza internamente a la función factorial(long long int) para hallar el factorial de un número
 *
 * \param Número al que se le va a intentar calcular el factorial
 * \return Devuelve el resultado del factorial o -1 si no se lo pudo hallar
 *
 */
long long int factorizar(float);
